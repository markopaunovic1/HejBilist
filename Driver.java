public class Driver {

    public String name;
    public int age;

    public Driver(String name, int age) {  // constructor Driver
        this.name = name;
        this.age = age;
    }

    public String getName(String name) {  // getName
        return name;
    }

    public void setName(String name) {  //setName
        this.name = name;
    }

    public int getAge(int age) {  //getName
        return age;
    }

    public void setAge(int age) {  // setName
        this.age = age;
    }
}
